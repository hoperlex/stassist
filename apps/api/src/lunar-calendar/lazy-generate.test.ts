/**
 * `shiftDateIso` — чистая функция (без I/O), используется `ensureAstroCalendarMonth` (находка
 * [lunnyj-kalendar-empty]) для вычисления «разогревочного» дня ДО начала месяца (нужен
 * `buildAstroCalendarWindow`, чтобы у 1-го числа был корректный «предыдущий день» для ингрессов).
 * `ensureAstroCalendarMonth` сама трогает БД — покрывается вручную/integration-тестами (см. §1
 * конвенций реализации), не unit-тестами; здесь тестируется только детерминированная арифметика.
 */
import { describe, expect, it } from 'vitest';
import { shiftDateIso } from './lazy-generate.js';

describe('shiftDateIso', () => {
  it('на -1 день внутри месяца', () => {
    expect(shiftDateIso('2026-08-15', -1)).toBe('2026-08-14');
  });

  it('на -1 день через границу месяца (1-е число → последний день предыдущего)', () => {
    expect(shiftDateIso('2026-08-01', -1)).toBe('2026-07-31');
  });

  it('на -1 день через границу года (1 января → 31 декабря предыдущего года)', () => {
    expect(shiftDateIso('2026-01-01', -1)).toBe('2025-12-31');
  });

  it('на -1 день через границу високосного февраля (1 марта → 29 февраля)', () => {
    expect(shiftDateIso('2028-03-01', -1)).toBe('2028-02-29');
  });
});
