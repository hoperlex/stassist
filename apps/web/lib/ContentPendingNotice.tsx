import { Alert } from 'antd';

/**
 * Честный empty-state для контента, которым владеет Ф4 (тексты пар совместимости, значения
 * арканов, краткие значения чисел — см. docs/roadmap/31-конвенции-реализации.md §6 «карта
 * владения seed-контентом»). НЕ фейковый текст — явная пометка «наполняется» (§6, «правило
 * непустоты»: расчёт/структура работают, трактовка — в разработке).
 */
export function ContentPendingNotice({ what }: { what: string }): React.JSX.Element {
  return (
    <Alert
      type="warning"
      showIcon
      style={{ marginTop: 16, marginBottom: 16 }}
      message="Текстовые трактовки наполняются"
      description={`${what} — расчёт и структура уже работают; развёрнутые текстовые трактовки готовит редакция и появятся в ближайших обновлениях.`}
    />
  );
}
