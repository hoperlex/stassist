export const meta = {
  name: 'editorial-corpus-wave',
  description: 'Многоагентная редактура батчей корпуса «Зодиакум»: editor → verifier, запись reviewed-JSON на диск',
  phases: [
    { title: 'Редактура', detail: 'агент-редактор пишет reviewed-JSON по батчу' },
    { title: 'Верификация', detail: 'агент-верификатор проверяет тон/факты/уникальность и правит' },
  ],
}

// args — одно из:
//   • массив дескрипторов батчей { batchId, domain, category, count, lengthBand:[min,max],
//     manifestPathAbs, reviewedPathAbs };
//   • строка-путь к .json-файлу с таким массивом (script не имеет fs → грузим агентом-загрузчиком).
const BATCH_ITEM = {
  type: 'object',
  additionalProperties: false,
  properties: {
    batchId: { type: 'string' },
    domain: { type: 'string' },
    category: { type: 'string' },
    count: { type: 'number' },
    lengthBand: { type: 'array', items: { type: 'number' } },
    manifestPathAbs: { type: 'string' },
    reviewedPathAbs: { type: 'string' },
  },
  required: ['batchId', 'domain', 'category', 'count', 'lengthBand', 'manifestPathAbs', 'reviewedPathAbs'],
}

let parsedArgs = args
if (typeof parsedArgs === 'string' && !parsedArgs.trim().startsWith('[')) {
  const filePath = parsedArgs.trim()
  const loaded = await agent(
    `Прочитай JSON-файл инструментом Read: ${filePath}. Это массив дескрипторов батчей. Верни его СОДЕРЖИМОЕ как есть (все элементы, без изменений).`,
    { label: 'load-batches', phase: 'Загрузка', agentType: 'general-purpose', schema: { type: 'object', additionalProperties: false, properties: { batches: { type: 'array', items: BATCH_ITEM } }, required: ['batches'] } },
  )
  parsedArgs = loaded && Array.isArray(loaded.batches) ? loaded.batches : []
} else if (typeof parsedArgs === 'string') {
  try {
    parsedArgs = JSON.parse(parsedArgs)
  } catch {
    parsedArgs = []
  }
}
const batches = Array.isArray(parsedArgs) ? parsedArgs : []
if (!batches.length) {
  log('Пустой список батчей — нечего редактировать.')
  return { batches: 0, results: [] }
}
log(`Батчей к редактуре: ${batches.length}`)

const RULES = `ПРАВИЛА КАЧЕСТВА (соблюдай ЖЁСТКО, это редакторский продовый уровень):
1. Живой, богатый русский язык. Конкретика и образы вместо «воды» и общих слов. Без
   эзотерического «тумана», канцелярита и штампов-заполнителей («это одна из граней личности…»).
2. Анти-галлюцинации: текст — это ИНТЕРПРЕТАЦИЯ архетипа, а не факт о мире. НЕ выдумывай даты,
   числа, проценты, «исследования», цитаты, имена, исторические «факты». Астрология/нумерология
   НЕ предсказывают конкретные события — они описывают склонности и смыслы.
3. Не-фатализм: формулируй как «склонность», «тенденция», «может проявляться как…», «располагает
   к…». НИКОГДА не «вас ждёт», «вы обязательно», «это случится». Подчёркивай свободу выбора и
   что проявление зависит от осознанности и всей карты целиком.
4. ЗАПРЕЩЕНО: медицинские диагнозы и рекомендации лечения; предсказания смерти/несчастных случаев;
   гарантии наступления событий (брак, деньги, ребёнок, работа); темы «порчи»/«сглаза»/их снятия.
   (Архетипические метафоры вроде 13-го Аркана «Смерть» = трансформация — допустимы как символ.)
   В дисклеймерах предпочитай формулировки без слов-триггеров: «не приговор» (а не «не диагноз»),
   «не обещает»/«не предопределяет» (а не «не гарантирует»), «самочувствие/бодрость» (а не «болезнь»).
5. АНТИДУБЛЯЖ (критично): весь батч — родственные ключи (например одна планета во всех 12 знаках,
   или один аркан во всех позициях). НЕ пиши их по одному шаблону с подстановкой слова. Варьируй
   ЗАЧИН (первое предложение — у каждой единицы свой, не «X в знаке Y…» подряд), структуру, ритм,
   образный ряд, угол захода. Соседние тексты не должны читаться как клоны.
6. Тон — поддерживающий, уважительный, взрослый; без токсичности, без сюсюканья, без гадательного
   пафоса. Пиши так, будто объясняешь умному взрослому читателю.
7. Название портала — «Зодиакум» (упоминай только если уместно; обычно не нужно). Никогда не «Stassist».
8. СМЫСЛ из поля "brief" каждой единицы СОХРАНИ (та же планета/знак/дом/аркан/число/камень) — но
   выражай его заново, своим текстом, а не перефразом черновика.`

function domainInstructions(domain) {
  switch (domain) {
    case 'chunks':
      return `ФОРМАТ ВЫВОДА (chunks): JSON-объект { "<key>": "<редакторский текст>" }. Ключи — ТОЧНО из
поля "key" манифеста. Текст — сплошной (без markdown-заголовков), 1–4 абзаца по ориентиру длины.`
    case 'compat':
      return `ФОРМАТ ВЫВОДА (совместимость): JSON-объект { "<id>": "<bodyMd текста пары>" }, id — из поля
"id" манифеста ("<signA>__<signB>"). Опиши динамику пары знаков честно (и сильные стороны, и
трения), избегай приговоров. ОБЯЗАТЕЛЬНО заверши мыслью, что реальная совместимость определяется
полными натальными картами обоих партнёров, а знак — лишь общий ориентир. Можно лёгкий markdown.`
    case 'wiki':
      return `ФОРМАТ ВЫВОДА (вики): JSON-объект { "<slug>": { "title": "<заголовок>", "bodyMd": "<markdown>" } }.
slug — из поля "slug" манифеста. bodyMd — полноценная markdown-статья, начинается с "## <Заголовок>",
далее 2–5 абзацев, при уместности — подзаголовки "### ..." и списки. Расширь смысл черновика (brief)
до связной статьи. Для раздела nakshatras — ЧЕСТНЫЙ каркас: назови накшатру и общий смысл, но НЕ
выдумывай детали джйотиша (падас, точные градусы, мифологию) — это заполнит специалист позже.`
    case 'stones':
      return `ФОРМАТ ВЫВОДА (камни): JSON-объект
{ "<slug>": { "propertiesMd": "...", "suitableMd": "..."|null, "unsuitableMd": "..."|null } }.
slug — из поля "slug" манифеста. Используй ТОЛЬКО факты из полей "facts" (цвет/знаки/планеты/
назначения) и "brief" — НЕ выдумывай минералогию, месторождения, «лечебные» свойства. Сохрани
дисклеймер: свойства камня — традиционные представления, а не медицинский/научный факт; решения о
здоровье — со специалистом. suitableMd/unsuitableMd — короткие практичные заметки (или null, если
нечего добавить фактически).`
    default:
      return ''
  }
}

function editorPrompt(b) {
  return `Ты — главный редактор русскоязычного астрологического портала «Зодиакум». Доведи черновой
батч корпуса до редакторского продового уровня.

ШАГ 1. Прочитай манифест батча (инструмент Read): ${b.manifestPathAbs}
Это JSON с массивом "entries". У каждой единицы есть корректный СМЫСЛ (поле "brief") и поля
идентификации (key/id/slug/facts).

ШАГ 2. Для КАЖДОЙ единицы батча напиши уникальный редакторский текст. Ориентир длины —
${b.lengthBand[0]}–${b.lengthBand[1]} символов (не догма, но держись рядом). Категория: ${b.category}.

${RULES}

${domainInstructions(b.domain)}

ШАГ 3. Запиши результат инструментом Write в файл: ${b.reviewedPathAbs}
Содержимое — СТРОГО валидный JSON нужной формы (см. формат выше), покрывающий ВСЕ ${b.count}
единиц батча, ключи/slug/id — ровно как в манифесте. Никакого текста вне JSON. Экранируй кавычки
и переводы строк корректно (это JSON).

Верни краткое summary (batchId и сколько единиц записал).`
}

function verifierPrompt(b) {
  return `Ты — строгий редактор-верификатор портала «Зодиакум». Проверь готовый батч «${b.batchId}».

ШАГ 1. Прочитай (Read): результат редактора ${b.reviewedPathAbs} и исходный манифест
${b.manifestPathAbs}.

ШАГ 2. Проверь КАЖДУЮ единицу по критериям:
- покрыты ВСЕ ${b.count} единиц манифеста, ключи/slug/id совпадают, JSON валиден, тексты непустые;
- длина примерно в диапазоне ${b.lengthBand[0]}–${b.lengthBand[1]} символов (допуск ±30%);
- НЕТ запрещённого: мед.диагнозы/лечение, предсказание смерти/несчастных случаев, гарантии
  событий, «порча»/«сглаз»;
- НЕТ фатализма («вас ждёт», «вы обязательно», «это случится»);
- НЕТ выдуманных фактов/дат/чисел/цитат/«исследований»;
- смысл соответствует "brief" (та же планета/знак/дом/аркан/число/камень);
- УНИКАЛЬНОСТЬ: зачины (первые предложения) и структура НЕ повторяются между единицами батча —
  это главный риск, проверь особенно тщательно;
- бренд «Зодиакум» (нигде не «Stassist»).

ШАГ 3. Если нашёл нарушения — ПЕРЕПИШИ проблемные единицы сам и ПЕРЕЗАПИШИ файл
${b.reviewedPathAbs} целиком инструментом Write (хорошие единицы оставь как есть, формат JSON
сохрани). Если всё чисто — файл не трогай.

Верни summary: {batchId, checked, rewritten (сколько единиц переписал), notes (кратко о проблемах)}.`
}

const EDIT_SUMMARY = {
  type: 'object',
  additionalProperties: false,
  properties: {
    batchId: { type: 'string' },
    written: { type: 'number' },
    note: { type: 'string' },
  },
  required: ['batchId', 'written'],
}

const VERIFY_SUMMARY = {
  type: 'object',
  additionalProperties: false,
  properties: {
    batchId: { type: 'string' },
    checked: { type: 'number' },
    rewritten: { type: 'number' },
    notes: { type: 'string' },
  },
  required: ['batchId', 'checked', 'rewritten'],
}

const results = await pipeline(
  batches,
  (b) => agent(editorPrompt(b), { label: `edit:${b.batchId}`, phase: 'Редактура', agentType: 'general-purpose', schema: EDIT_SUMMARY }),
  (edit, b) =>
    agent(verifierPrompt(b), { label: `verify:${b.batchId}`, phase: 'Верификация', agentType: 'general-purpose', schema: VERIFY_SUMMARY }).then((v) => ({
      batchId: b.batchId,
      domain: b.domain,
      edit,
      verify: v,
    })),
)

const ok = results.filter(Boolean)
log(`Готово батчей: ${ok.length}/${batches.length}`)
return {
  batches: batches.length,
  done: ok.length,
  results: ok,
}
