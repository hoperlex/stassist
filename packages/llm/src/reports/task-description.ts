/**
 * Текст задачи для LLM по типу разбора/сфере (см. f4-llm-конвейер.md req.5). Отдельный чистый
 * модуль — легко покрыть тестами и версионировать независимо от сборки промта.
 */
import type { NatalFullSphere, ReportKind } from '@stassist/shared';

const NATAL_FULL_SPHERE_RU: Record<NatalFullSphere, string> = {
  personality: 'личность и характер',
  relationships: 'отношения и близость',
  career: 'карьеру и предназначение в деле',
  resources: 'ресурсы, финансы и ценности',
  growth_tasks: 'задачи роста и точки развития (акцент на узлах/Хироне)',
};

export function buildTaskDescription(kind: ReportKind, opts: { sphere?: NatalFullSphere; question?: string } = {}): string {
  switch (kind) {
    case 'big3':
      return (
        'Напиши краткий (3-4 абзаца) обзорный разбор «большой тройки»: Солнце, Луна' +
        ' (и Асцендент, если он есть в данных). Структура: заголовок «Ваша большая тройка», ' +
        'абзац про Солнце, абзац про Луну, абзац про Асцендент (или явное указание, что время ' +
        'неизвестно и Асцендент не учтён), короткий итоговый абзац.'
      );
    case 'natal_full': {
      const sphereRu = opts.sphere ? NATAL_FULL_SPHERE_RU[opts.sphere] : 'выбранную сферу';
      return `Напиши развёрнутый (5-8 абзацев) раздел разбора натальной карты на тему «${sphereRu}», опираясь на релевантные факты и фрагменты корпуса.`;
    }
    case 'synastry':
      return 'Напиши разбор совместимости (синастрии) по межличностным аспектам между двумя картами — сильные стороны пары и точки, требующие внимания.';
    case 'solar_year':
      return 'Напиши прогноз на предстоящий соляр (личный год) — основные темы и акценты периода.';
    case 'transit_month':
      return 'Напиши обзор ключевых транзитов текущего месяца применительно к натальной карте.';
    case 'matrix_full':
      return 'Напиши развёрнутый разбор матрицы судьбы по арканам предоставленных позиций.';
    case 'custom_question':
      return `Ответь на вопрос пользователя, опираясь на факты карты и фрагменты корпуса: «${opts.question ?? ''}».`;
    case 'order':
      return 'Подготовь развёрнутый персональный разбор по заказу пользователя.';
  }
}
