/**
 * Regex-слой фильтра запрещённого контента (см. незыблемое правило №3 f4-промта и раздел
 * «Верификация»: провокационные кейсы «когда я умру», «сниму ли порчу», медицинские вопросы →
 * мягкий отказ с перенаправлением). Это ПЕРВАЯ линия защиты — офлайн, без сети, гоняется и на
 * входящем вопросе пользователя (chat/intent-router.ts — короткое замыкание до вызова LLM), и на
 * сгенерированном тексте отчёта (postprocess.ts — после генерации). LLM-классификатор (вторая
 * линия, семантическая) — интеграционный шаг, требует живого провайдера, вне юнит-гейта.
 */
export type ForbiddenCategory = 'death' | 'medical' | 'curse' | 'guarantee';

export interface ForbiddenMatch {
  category: ForbiddenCategory;
}

// Намеренно широкие (recall-first) — это первая, офлайн-линия защиты поверх regex. Исключение —
// «death»: бытовое слово «смерть» легитимно встречается в контенте корпуса (13-й Аркан Таро
// традиционно называется «Смерть» — метафора трансформации, см. corpus/phrase-banks.ts), поэтому
// категория «death» требует явно вопросительно-предсказательного контекста («когда я умру»,
// «дата смерти» и т.п.), а не голого слова — иначе фильтр ложно резал бы легитимный контент
// арканов при пост-обработке отчётов matrix_full.
const FORBIDDEN_PATTERNS: Array<{ category: ForbiddenCategory; re: RegExp }> = [
  {
    category: 'death',
    re: /когда\s+я\s+.{0,10}умр|умру\s+ли|я\s+умру|предскаж\w*.{0,20}смерт\w*|дат[ауы]\s+сме?рти|причин\w*\s+сме?рти|погибну\s+ли|я\s+погибну/iu,
  },
  { category: 'medical', re: /диагноз\w*|вылечи\w*|лечени\w*|болезн\w*/iu },
  { category: 'curse', re: /порч\w*|сглаз\w*|привор[оа]т\w*/iu },
  { category: 'guarantee', re: /гарантир\w*|точно\s+ли|обязательно\s+ли\s+(сбуд|случ)/iu },
];

// Категории, для которых ОТРИЦАНИЕ/оговорка непосредственно перед триггером снимает срабатывание.
// Мотив: добросовестный текст-дисклеймер («я НЕ ставлю диагнозы», «астрология ничего НЕ гарантирует»,
// «портал НЕ занимается порчей») — это ПРАВИЛЬНОЕ поведение, а не запрещённый контент; наивный
// regex.test() ложно резал такие формулировки в редакторском корпусе и в мягких отказах (см. промт
// text-корпус-редактура.md, блок C: «учесть отрицание, не рубить добросовестный текст»). Для «death»
// это не нужно — паттерн уже сужен до вопросительно-предсказательного контекста.
const NEGATABLE: ReadonlySet<ForbiddenCategory> = new Set(['medical', 'curse', 'guarantee']);

// Отрицание/замена как отдельное слово в клаузе перед триггером: «не», «ни», «без», «вместо».
// (JS `\b` — ASCII-only даже с флагом `u`, поэтому границу слова задаём явно через разделители и
// пробел после слова, а не `\b`.)
const NEGATION_IN_CLAUSE = /(?:^|[\s(«„"'—-])(?:не|ни|без|вместо)\s/iu;
// Границы клаузы — конец предложения/точка с запятой/перенос строки (запятую НЕ считаем границей:
// «не занимается темами порчи и сглаза» — одна клауза, отрицание «не» покрывает оба триггера).
const CLAUSE_BOUNDARY = /[.!?;\n]/g;

/**
 * true, если у паттерна есть хотя бы одно вхождение, НЕ находящееся в клаузе с отрицанием (для
 * NEGATABLE-категорий). Клауза вхождения = отрезок от предыдущей границы предложения до вхождения;
 * если в нём есть «не/ни/без/вместо» — это добросовестная оговорка, а не запрос/утверждение.
 */
function hasNonNegatedMatch(text: string, re: RegExp): boolean {
  const global = new RegExp(re.source, re.flags.includes('g') ? re.flags : `${re.flags}g`);
  let m: RegExpExecArray | null;
  while ((m = global.exec(text)) !== null) {
    let clauseStart = 0;
    let bm: RegExpExecArray | null;
    CLAUSE_BOUNDARY.lastIndex = 0;
    while ((bm = CLAUSE_BOUNDARY.exec(text)) !== null && bm.index < m.index) clauseStart = bm.index + 1;
    const clause = text.slice(clauseStart, m.index);
    if (!NEGATION_IN_CLAUSE.test(clause)) return true;
    if (m.index === global.lastIndex) global.lastIndex++; // защита от вхождений нулевой длины
  }
  return false;
}

export function detectForbidden(text: string): ForbiddenMatch[] {
  const matches: ForbiddenMatch[] = [];
  for (const { category, re } of FORBIDDEN_PATTERNS) {
    const hit = NEGATABLE.has(category) ? hasNonNegatedMatch(text, re) : re.test(text);
    if (hit) matches.push({ category });
  }
  return matches;
}

export const SOFT_REFUSAL_RU: Record<ForbiddenCategory, string> = {
  death:
    'Я не предсказываю даты смерти, несчастные случаи или иные фатальные события — это выходит за рамки ' +
    'информационно-развлекательной астрологии. Если вас тревожат такие мысли, пожалуйста, обратитесь к психологу.',
  medical:
    'Я не ставлю медицинские диагнозы и не даю рекомендаций по лечению. По вопросам здоровья, пожалуйста, ' +
    'обратитесь к врачу — астрология может говорить лишь об общих психологических склонностях.',
  curse:
    'Портал «Зодиакум» не занимается темами «порчи», «сглаза» и не предлагает их снятие — это не относится к ' +
    'нашим услугам. Если вас беспокоит тревога или навязчивые мысли, полезнее обратиться к психологу.',
  guarantee:
    'Астрология и нумерология описывают тенденции и склонности, а не гарантируют конкретные события — я не ' +
    'могу подтвердить, что что-то «точно» произойдёт.',
};

export function buildSoftRefusalText(matches: ForbiddenMatch[]): string {
  const unique = [...new Set(matches.map((m) => m.category))];
  return unique.map((c) => SOFT_REFUSAL_RU[c]).join(' ');
}
