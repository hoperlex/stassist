/**
 * Верификационный набор промта Ф7 («Модерация: 20 провокационных постов… ≥18 перехвачены
 * автоклассификатором, остальные — вручную из очереди», см. раздел «Верификация» промта). Набор
 * ниже — 20 примерных провокационных формулировок (по 4 запрещённым категориям + оскорбления),
 * написанных для теста детерминированного классификатора (НЕ реальные жалобы пользователей).
 */
import { describe, expect, it } from 'vitest';
import { classifyUgcText } from './ugc-classifier.js';

const PROVOCATIVE_POSTS: string[] = [
  // Предсказания смерти/болезней (5)
  'Когда я умру, подскажите по карте?',
  'Скажите точно дату моей смерти по натальной карте.',
  'Умру ли я в этом году, у меня плохие транзиты?',
  'У меня рак, врачи не помогают — поставьте диагноз по гороскопу.',
  'Какой болезнью я заболею по моей карте здоровья?',
  // Порча/привороты (5)
  'Как навести порчу на бывшего мужа по его знаку?',
  'Можно ли сделать приворот на любимого через натальную карту?',
  'Ищу мастера, который снимет с меня порчу.',
  'Хочу приворот, чтобы он вернулся, подскажите ритуал.',
  'На меня навели сглаз, как узнать по карте кто это сделал?',
  // Медицинские директивы (3)
  'Прекратите принимать лекарства, звёзды говорят что вылечитесь сами.',
  'Не ходите к врачу, у вас просто ретроградный Меркурий, само пройдёт.',
  'Какое лечение мне назначить по гороскопу вместо антибиотиков?',
  // Финансовые директивы (3)
  'Вложите все деньги в этот проект — звёзды гарантируют доход 100% прибыли.',
  'Продайте квартиру и переведите деньги мне, я astro-консультант с гарантией.',
  'Возьмите срочный займ сейчас, транзит Юпитера гарантирует успех.',
  // Оскорбления (4)
  'Ты полный дурак, раз веришь в этот гороскоп.',
  'Какой же ты идиот, что спрашиваешь такое.',
  'Автор поста — ничтожество и мразь.',
  'Такие вопросы задают только дебилы.',
];

describe('classifyUgcText — верификационный набор промта Ф7 (20 постов)', () => {
  it('перехватывает ≥18 из 20 провокационных постов (порог промта)', () => {
    const caught = PROVOCATIVE_POSTS.filter((text) => classifyUgcText(text).flagged);
    expect(caught.length).toBeGreaterThanOrEqual(18);
  });

  it('каждая из 5 запрещённых категорий промта представлена хотя бы одним перехваченным примером', () => {
    const allReasons = new Set(PROVOCATIVE_POSTS.flatMap((text) => classifyUgcText(text).reasons));
    expect(allReasons).toContain('death_or_illness_prediction');
    expect(allReasons).toContain('curse_or_love_spell');
    expect(allReasons).toContain('medical_directive');
    expect(allReasons).toContain('financial_directive');
    expect(allReasons).toContain('insult');
  });

  it('НЕ флагует нейтральный вопрос о натальной карте (низкий false-positive на обычном контенте)', () => {
    const benign = classifyUgcText('Подскажите, что означает Венера в 7 доме моей натальной карты?');
    expect(benign.flagged).toBe(false);
  });

  it('НЕ флагует упоминание 13-го Аркана «Смерть» (метафора трансформации, легитимный контент вики)', () => {
    const benign = classifyUgcText('13-й Аркан Таро называется «Смерть» и символизирует трансформацию.');
    expect(benign.flagged).toBe(false);
  });

  it('flagged=false и reasons=[] для пустого/безобидного текста', () => {
    expect(classifyUgcText('Привет! Только начинаю изучать астрологию.')).toEqual({ flagged: false, reasons: [] });
  });
});
