/**
 * Циклическая таблица «буква → число» 1–9: значение = (порядковый номер буквы в алфавите − 1)
 * по модулю 9, плюс 1. Например: А=1 … З=9, И=1 (10-я буква) … Щ=9 (27-я), Ъ=1 … Я=6 (33-я).
 */
export const LETTER_VALUES: Readonly<Record<string, number>> = Object.fromEntries(
  CYRILLIC_ALPHABET.map((letter, index) => [letter, (index % 9) + 1]),
);

/**
 * Гласные для числа души (Soul Urge) — состав зафиксирован явно промтом фазы:
 * А Е Ё И О У Ы Э Ю Я. Все прочие буквы алфавита (включая Й, Ъ, Ь) считаются «не гласными»
 * и участвуют в числе личности.
 */
export const VOWELS: ReadonlySet<string> = new Set([
  'А',
  'Е',
  'Ё',
  'И',
  'О',
  'У',
  'Ы',
  'Э',
  'Ю',
  'Я',
]);
