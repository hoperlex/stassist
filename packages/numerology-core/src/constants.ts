/**
 * Таблица «буква → число» для пифагорейской системы нумерологии имени (кириллица) и связанные
 * с ней константы. Единого общепринятого стандарта для кириллицы НЕТ (см.
 * docs/research/06-нумерология-и-минералы.md §1): конкурируют «порядковая» таблица (А=1…Я=33)
 * и «циклическая» 1–9 (по аналогии с латинской пифагорейской: A,J,S=1 … I,R=9). Портал фиксирует
 * ЦИКЛИЧЕСКУЮ таблицу — значение буквы равно остатку от деления её порядкового номера в алфавите
 * на 9 (диапазон 1–9, круг повторяется).
 *
 * Осознанные решения по спорным буквам (зафиксированы по прямому указанию промта фазы):
 *  - Ё — ОТДЕЛЬНАЯ буква (не приравнивается к Е), стоит в алфавите сразу после Е и получает
 *    следующее по циклу значение (7).
 *  - Й — считается СОГЛАСНОЙ и участвует в общем цикле наравне с остальными буквами (не
 *    пропускается и не приравнивается к И).
 *  - Ъ и Ь — не гласные (не входят в VOWELS) и потому попадают в «согласные» для числа личности;
 *    формально это не согласные звуки, но для полноты алфавита и детерминированности расчёта
 *    они участвуют в общем цикле наравне с прочими буквами.
 *
 * ВАЖНО: это РАБОЧАЯ ЭВРИСТИКА, а не сверенный на 100% факт — таблица вынесена в именованную
 * константу намеренно, чтобы редактор мог скорректировать её одним местом, не трогая логику
 * расчёта в core-numbers.ts. См. README.md пакета, раздел «Известные ограничения».
 */

/** Русский алфавит в стандартном порядке (33 буквы, Ё — отдельная буква после Е). */
