/**
 * Нормализация текста имени для нумерологических расчётов: верхний регистр, только буквы
 * кириллицы А-Я и Ё (пробелы, дефисы, цифры, латиница и любые прочие символы отбрасываются).
 */
export function normalizeName(input: string): string {
  return input.toUpperCase().replace(/[^А-ЯЁ]/g, '');
}
