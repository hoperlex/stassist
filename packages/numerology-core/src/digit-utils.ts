/**
 * Общие примитивы для работы с цифрами чисел — используются во всех модулях пакета
 * (число жизненного пути, числа имени, психоматрица, матрица судьбы, персональные циклы).
 * Здесь нет I/O и нет обращений к дате/времени процесса — только чистая арифметика.
 */

/**
 * Сумма цифр целого числа за ОДИН проход (без повторной редукции).
 * Отрицательные и дробные значения приводятся к неотрицательному целому (`Math.trunc` + модуль) —
 * в доменных расчётах пакета отрицательных входов не ожидается, это защитная мера.
 */
export function sumDigits(value: number): number {
  const normalized = Math.abs(Math.trunc(value));
  let sum = 0;
  for (const char of String(normalized)) {
    sum += Number(char);
  }
  return sum;
}

/**
 * Редукция числа сквозным суммированием цифр с ОСТАНОВКОЙ на мастер-числах 11 и 22
 * (33 и другие «повторы» мастер-числами НЕ считаются — по прямому решению портала, см. README
 * пакета и docs/research/06-нумерология-и-минералы.md §1: используется метод «сквозного
 * сложения всех цифр», а не «метод трёх циклов»).
 *
 * Пример: 29 → (не 11/22, редуцируем) → 2+9=11 → (это мастер-число, останавливаемся) → 11.
 */
export function reduceKeepMasterNumbers(value: number): number {
  let n = Math.abs(Math.trunc(value));
  while (n > 9 && n !== 11 && n !== 22) {
    n = sumDigits(n);
  }
  return n;
}

/**
 * Полная редукция до одной цифры (1–9), БЕЗ сохранения мастер-чисел — применяется там, где
 * по общепринятой практике мастер-числа не используются (персональный год/месяц/день).
 */
export function reduceToSingleDigit(value: number): number {
  let n = Math.abs(Math.trunc(value));
  while (n > 9) {
    n = sumDigits(n);
  }
  return n;
}
